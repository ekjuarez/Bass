    public static double fahrenheitToCelsius (double fahrenheit) {
        return (fahrenheit - 32) * (5.0 / 9.0);
    }
    public static double celsiusToFahrenheit (double celsius) {
        return ((9.0 / 5.0) * celsius) + 32;
    }
